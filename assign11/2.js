// using switch statement
let grade = prompt("enter the grade");

switch(grade){
    case 'A':
        console.log(`your marks is 100 and grade is ${grade}`);
        break;
    case 'B':
        console.log(`your marks is 90 and grade is ${grade}`);
        break;
    case 'C':
        console.log(`your marks is 80 and grade is ${grade}`);
        break;
    case 'D':
         console.log(`your marks is below 80 and grade is Fail`);
         break;

}